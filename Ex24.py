num = [1,2,3,4,5,6,7,8,9]
#索引值 0,1,2,3,4,5,6,7,8
n1 = num[:5] #抓到索引值5之前結束
n2 = num[2:7] #初始為索引值2,抓到索引值7之前結束
n3 = num[2:]
n4 = num[0:6:2] #初始值為0,間隔為2,抓到索引值6之前結束

print(n1)
print(n2)
print(n3)
print(n4)














